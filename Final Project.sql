DROP DATABASE IF EXISTS Sales;
DROP DATABASE IF EXISTS Production;

-- Create Sales Database
CREATE DATABASE Sales;

-- Create Production Database
CREATE DATABASE Production;

-- Use Production Database
USE Production;

-- Create Production.Brands Table
 CREATE TABLE Production.Brands (
    brand_id INT PRIMARY KEY NOT NULL,
    brand_name VARCHAR(50) NULL DEFAULT NULL
);

-- Create Production.Categories Table
CREATE TABLE Production.Categories (
    category_id INT PRIMARY KEY NOT NULL,
    category_name VARCHAR(50) NULL DEFAULT NULL
);

-- Create Production.Products Table
CREATE TABLE Production.Products (
    product_id INT PRIMARY KEY NOT NULL,
    product_name VARCHAR(100)  NULL DEFAULT NULL,
    brand_id INT,
    category_id INT,
    model_year INT,
    list_price DECIMAL(10, 2),
    FOREIGN KEY (brand_id) REFERENCES Production.Brands(brand_id),
	FOREIGN KEY (category_id) REFERENCES Production.Categories(category_id)
    );
   
    -- Create Sales. Stores Table
CREATE TABLE Sales.Stores (
    store_id INT PRIMARY KEY NOT NULL,
    store_name VARCHAR(100),
    phone VARCHAR(15),
    email VARCHAR(100),
    street VARCHAR(100),
    city VARCHAR(50),
    state VARCHAR(50),
    zip_code VARCHAR(10)
);

-- Create Production.Stocks Table
CREATE TABLE Production.Stocks (
    store_id INT  NOT NULL,
    product_id INT  NOT NULL,
    quantity INT NULL DEFAULT NULL,
    PRIMARY KEY (store_id, product_id),
    FOREIGN KEY (store_id) REFERENCES Sales.Stores(store_id),
    FOREIGN KEY (product_id) REFERENCES Production.Products(product_id)
);

USE Sales;

-- Create Sales.Customers Table
CREATE TABLE Sales.Customers (
    customer_id INT PRIMARY KEY NOT NULL,
    first_name VARCHAR(50),
    last_name VARCHAR(50),
    phone VARCHAR(15),
    email VARCHAR(100),
    street VARCHAR(100),
    city VARCHAR(50),
    state VARCHAR(50),
    zip_code VARCHAR(10)
);

-- Create Sales.Staffs Table
CREATE TABLE Sales.Staffs (
    staff_id INT PRIMARY KEY NOT NULL,
    first_name VARCHAR(50),
    last_name VARCHAR(50),
    email VARCHAR(100),
    phone VARCHAR(15),
    active VARCHAR(15),
    store_id INT,
    manager_id INT,
    FOREIGN KEY (manager_id) REFERENCES Sales.Staffs(staff_id),
    FOREIGN KEY (store_id) REFERENCES Sales.Stores(store_id)
);

-- Create Sales.Orders Table
CREATE TABLE Sales.Orders (
    order_id INT PRIMARY KEY NOT NULL,
    customer_id INT,
    order_status VARCHAR(50),
    order_date DATE,
    Shipped_date DATE,
    required_date DATE,
    store_id INT,
    staff_id INT,
    FOREIGN KEY (customer_id) REFERENCES Sales.Customers(customer_id),
    FOREIGN KEY (store_id) REFERENCES Sales.Stores(store_id),
    FOREIGN KEY (staff_id) REFERENCES  Sales.Staffs(staff_id)
    );
    
    -- Create Sales.OrderItems Table
CREATE TABLE Sales.OrderItems (
    order_id INT NOT NULL,
    item_id INT NOT NULL,
    product_id INT,
    quantity INT,
    list_price DECIMAL(10, 2),
    discount VARCHAR(10),
    PRIMARY KEY (order_id, item_id),
    FOREIGN KEY (order_id) REFERENCES Sales.Orders(order_id),
    FOREIGN KEY (product_id) REFERENCES Production.Products(product_id)
);

